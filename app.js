// --- 학생용 실시간 채널 연결 ---
function setupStudentRealtime() {
  realtimeChannel = supabaseClient.channel(`room_${sessionPin}`);

  realtimeChannel
    .on('broadcast', { event: 'lock-settings' }, ({ payload }) => {
      // 교사가 조건을 잠갔을 때 작동
      onTeacherLockSettings(payload);
    })
    .on('broadcast', { event: 'unlock-settings' }, () => {
      // 교사가 조건을 풀었을 때 다시 로비 대기로
      showStudentLobbyWait();
    })
    .on('broadcast', { event: 'launch' }, () => {
      // 교사의 일괄 출발 발송 수신
      onLaunchTriggered();
    })
    .on('broadcast', { event: 'next-round' }, () => {
      // 다음 라운드 준비 리셋
      onNextRoundTriggered();
    })
    .subscribe(async (status) => {
      if (status === 'SUBSCRIBED') {
        myStudentId = 'student_' + Math.random().toString(36).substring(2, 9);

        // Presence로 학생 정보 트래킹 등록
        await realtimeChannel.track({
          role: 'student',
          id: myStudentId,
          nickname: myNickname
        });

        document.getElementById('student-login-overlay').style.display = 'none';
        document.getElementById('main-ui').style.pointerEvents = 'auto';
        showStudentLobbyWait();
      } else if (status === 'CHANNEL_ERROR') {
        alert("서버 연결 오류가 발생했습니다. PIN 번호를 확인해 주세요.");
      }
    });
}

