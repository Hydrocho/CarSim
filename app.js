// --- 챌린지 모드용 새로운 목표 생성 ---
function generateNewChallenge() {
  if (simulationState !== 'idle' && simulationState !== 'stopped') return;

  // 25m에서 95m 사이의 임의 정수 생성
  targetDistance = Math.floor(Math.random() * (95 - 25 + 1)) + 25;

  // 빨간 목표선 Z 위치 업데이트
  if (redLineMesh) redLineMesh.position.z = targetDistance;
  if (redLineSignpost) redLineSignpost.position.set(10 / 2 + 1.5, 0, targetDistance);

  // 도로 위 노면 글씨 Z 위치 업데이트 (목표 선 직전 배치)
  if (roadText) {
    roadText.position.z = targetDistance - 18.4;
  }

  safeSetText('target-distance-value', targetDistance.toFixed(1));

  // 반투명 목표 거리 모달 팝업 띄우기
  showTargetModal(targetDistance);

  resetSimulation();
}

// --- 반투명 목표 거리 모달 노출 제어 ---
function showTargetModal(dist) {
  const modal = document.getElementById('target-modal');
  const modalText = document.getElementById('modal-target-dist');
  if (modal && modalText) {
    modalText.innerText = dist.toFixed(1);
    modal.style.display = 'flex';
  }
}

function closeTargetModal() {
  const modal = document.getElementById('target-modal');
  if (modal) {
    modal.style.display = 'none';
  }
}

// 전역 스코프에 노출하여 HTML inline onclick에서 호출 가능하게 함
window.closeTargetModal = closeTargetModal;
window.showTargetModal = showTargetModal;

// --- 결과 안내 모달 노출 제어 ---
function showResultModal(actualDist, targetDist, diff, absDiff) {
  const modal = document.getElementById('result-modal');
  const modalTitle = document.getElementById('modal-result-title');
  const modalActual = document.getElementById('modal-actual-dist');
  const modalTarget = document.getElementById('modal-target-dist-result');
  const modalFeedback = document.getElementById('modal-result-feedback');

  if (!modal) return;

  modalActual.innerText = `${actualDist.toFixed(2)}m`;
  modalTarget.innerText = `${targetDist.toFixed(1)}m`;

  if (absDiff <= 0.6) {
    modalFeedback.className = 'result-feedback-box success';
    modalTitle.innerText = '🎯 미션 성공!';
    modalFeedback.innerHTML = `목표 정지선 오차 <strong>${absDiff.toFixed(2)}m</strong> 이내로 <strong>정확히 정차</strong>했습니다!`;
  } else {
    modalFeedback.className = 'result-feedback-box fail';
    if (diff > 0) {
      modalTitle.innerText = '💥 정지선 초과 (오버런)';
      modalFeedback.innerHTML = `목표 정지선보다 <strong>${diff.toFixed(2)}m 지나쳤습니다</strong>.`;
    } else {
      modalTitle.innerText = '⚠️ 제동 거리 미달 (조기 정지)';
      modalFeedback.innerHTML = `목표 정지선에 <strong>${Math.abs(diff).toFixed(2)}m 모자라게 멈췄습니다</strong>.`;
    }
  }

  modal.style.display = 'flex';
}

function closeResultModal() {
  const modal = document.getElementById('result-modal');
  if (modal) {
    modal.style.display = 'none';
  }
}

window.showResultModal = showResultModal;
window.closeResultModal = closeResultModal;

// --- 시뮬레이션 및 입력 UI 동기화 ---
function setupUIEventListeners() {
  const massInput = document.getElementById('mass-input');
  if (massInput) {
    massInput.addEventListener('blur', () => {
      let val = parseInt(massInput.value);
      if (isNaN(val)) val = 1500;
      val = Math.max(1000, Math.min(5000, val));
      massInput.value = val;
      mass = val;
      const massSlider = document.getElementById('mass-slider');
      if (massSlider) massSlider.value = val;
      updateCargoBoxes(mass);
      updateDashboard();
    });
  }
  const speedInput = document.getElementById('speed-input');
  if (speedInput) {
    speedInput.addEventListener('blur', () => {
      let val = parseInt(speedInput.value);
      if (isNaN(val)) val = 50;
      val = Math.max(10, Math.min(150, val));
      speedInput.value = val;
      initialSpeedKmh = val;
      const speedSlider = document.getElementById('speed-slider');
      if (speedSlider) speedSlider.value = val;
      updateDashboard();
    });
  }
}

// --- 대시보드 실시간 정보 갱신 ---
function updateDashboard(currentSpeedValKmh) {
  // 속도값 입력
  const displaySpeed = currentSpeedValKmh !== undefined ? currentSpeedValKmh : initialSpeedKmh;
  const displaySpeedMps = displaySpeed / 3.6;

  // 운동에너지 계산 (E = 0.5 * m * v^2)
  const energyJoules = 0.5 * mass * Math.pow(displaySpeedMps, 2);
  const energyKiloJoules = energyJoules / 1000;

  safeSetText('energy-value', energyKiloJoules.toFixed(1));

  // 운동에너지 그래프 게이지바 업데이트 (1000kg 10km/h 최소 ~ 5000kg 150km/h 최대 대비 비율)
  // 최대 에너지: 0.5 * 5000 * (41.67)^2 = 4,340,277 J (4340 kJ)
  const maxEnergy = 4340;
  const energyPercent = Math.min(100, (energyKiloJoules / maxEnergy) * 100);
  safeSetStyleWidth('energy-bar', `${energyPercent}%`);

  // 상태 배지 업데이트
  const badge = document.getElementById('status-badge');
  if (badge) {
    badge.className = 'status-badge'; // 기본 클래스 리셋

    if (simulationState === 'idle') {
      badge.innerText = '대기 중';
      badge.classList.add('state-idle');
    } else if (simulationState === 'driving') {
      badge.innerText = '가속 주행';
      badge.classList.add('state-driving');
    } else if (simulationState === 'braking') {
      badge.innerText = '제동 감속';
      badge.classList.add('state-braking');
    } else if (simulationState === 'stopped') {
      badge.innerText = '정지 완료';
      badge.classList.add('state-stopped');
    }
  }

  // 실시간 예상 제동 거리 계산 및 반영
  const predDist = (mass * Math.pow(displaySpeed, 2)) / 200000;
  safeSetText('predicted-distance-value', predDist.toFixed(1));
}

// --- 이징(Easing) 함수: 부드러운 카메라 전환용 ---
function easeInOutQuad(t) {
  return t < 0.5 ? 2 * t * t : 1 - Math.pow(-2 * t + 2, 2) / 2;
}

// --- 시뮬레이션 출발 ---
function startSimulation() {
  if (simulationState !== 'idle') return;

  // 출발 버튼 클릭 시 안내 모달 팝업 닫기
  closeTargetModal();

  // 슬라이더 및 입력 상자 조작 불가능하도록 락 걸기
  toggleInputs(true);

  simulationState = 'driving';
  initialSpeedMps = initialSpeedKmh / 3.6;
  currentSpeedMps = initialSpeedMps;
  brakingElapsedTime = 0;

  // 주행 중 사용자가 카메라를 돌려 구도가 깨지는 것을 방지하기 위해 마우스 조작 비활성화
  controls.enabled = false;

  // 버튼들 비활성화 설정
  safeSetDisabled('btn-start', true);
  safeSetDisabled('btn-reset', false);
  safeSetDisabled('btn-new-target', true);

  // 결과 창 리셋
  safeSetStyleDisplay('result-box', 'none');

  // 가상 환경 속도감 재세팅을 위해 시계 리셋
  clock.getDelta();
}

// --- 시뮬레이션 정지 및 원상 복구 ---
function resetSimulation() {
  simulationState = 'idle';
  currentSpeedMps = 0;

  // 결과 안내 모달 팝업 닫기
  closeResultModal();

  // 브레이크등 라이트 소등
  setBrakeLightsActive(false);

  // 대시보드 텍스트 복구
  safeSetText('actual-distance-value', '0.0');
  safeSetStyleDisplay('result-box', 'none');

  // 차량 위치 및 회전 바퀴 리셋
  resetCamera();

  // 컨트롤 락 해제
  toggleInputs(false);

  // 출발 버튼 활성화
  safeSetDisabled('btn-start', false);
  safeSetDisabled('btn-reset', true);
  safeSetDisabled('btn-new-target', false);

  updateDashboard();

  // 초기화 버튼 클릭 시 안내 모달 팝업 다시 띄우기
  showTargetModal(targetDistance);
}

function toggleInputs(disabled) {
  safeSetDisabled('mass-slider', disabled);
  safeSetDisabled('mass-input', disabled);
  safeSetDisabled('speed-slider', disabled);
  safeSetDisabled('speed-input', disabled);
}

// --- 제동 완수 및 결과 판정 ---
function showResults() {
  const finalDistance = truckGroup.position.z + 2.5; // 앞 범퍼(local Z=2.5) 기점으로 제동 거리 환산
  safeSetText('actual-distance-value', finalDistance.toFixed(2));

  // 제동 완료 후 사용자가 차량 근처에서 카메라 조작을 즐길 수 있도록 락 해제 및 타겟 갱신
  controls.target.copy(truckGroup.position).add(new THREE.Vector3(0, 0.8, 1.5));
  controls.enabled = true;
  controls.update();

  // 챌린지 성공 조건 판정 (목표선 대비 오차가 0.6m 이내인지 판단)
  const diff = finalDistance - targetDistance;
  const absDiff = Math.abs(diff);

  const resultBox = document.getElementById('result-box');
  if (resultBox) {
    if (absDiff <= 0.6) {
      resultBox.className = 'result-box success'; // 스타일 시트에 녹색 테두리 반영
      safeSetText('result-title', '🎯 미션 성공!');
      safeSetHTML('result-desc', `목표: ${targetDistance.toFixed(1)}m | 실제 제동: ${finalDistance.toFixed(2)}m<br>오차: <span style="font-weight:700; color:#4caf50;">${diff >= 0 ? '+' : ''}${diff.toFixed(2)}m</span>`);
    } else {
      resultBox.className = 'result-box fail'; // 스타일 시트에 적색 테두리 반영
      if (diff > 0) {
        safeSetText('result-title', '💥 정지선 초과 (오버런)');
        safeSetHTML('result-desc', `목표를 <span style="font-weight:700;">${diff.toFixed(2)}m</span> 초과했습니다.`);
      } else {
        safeSetText('result-title', '⚠️ 제동 거리 미달 (조기 정지)');
        safeSetHTML('result-desc', `목표보다 <span style="font-weight:700;">${Math.abs(diff).toFixed(2)}m</span> 덜 갔습니다.`);
      }
    }
    resultBox.style.display = 'block';
  }

  updateDashboard(0);

  // 결과 안내 팝업 모달 띄우기
  if (isMultiplayer) {
    reportStudentResult(finalDistance, diff);
  } else {
    showResultModal(finalDistance, targetDistance, diff, absDiff);
  }
}

// --- 메인 업데이트 루프 (Three.js 및 물리학 실행) ---
function update() {
  let dt = clock.getDelta();
  if (dt > 0.05) dt = 0.05; // 탭 전환 시 오류 방지용 캡핑

  if (userRole === 'teacher') {
    updateTeacherPhysics(dt);
  } else {
    // 1. 차량 상태별 물리학 프레임 갱신
    if (simulationState === 'driving') {
      // 검은 브레이크선 Z=0을 향해 등속 주행
      truckGroup.position.z += initialSpeedMps * dt;

      // 바퀴 굴러가기
      const wheelRotSpeed = initialSpeedMps / WHEEL_RADIUS;
      wheels.forEach(w => w.rotation.x += wheelRotSpeed * dt);

      // 카메라 3/4 뒷모습에서 90도 옆모습(Side view)으로 부드럽게 스윕
      // 앞 범퍼가 브레이크선(Z=0)에 닿을 때까지(출발지 Z=-30m에서 -2.5m까지 총 27.5m 이동) 비율 progress
      const progress = Math.max(0, Math.min(1, (truckGroup.position.z - START_Z) / (BRAKE_Z - 2.5 - START_Z)));
      const eased = easeInOutQuad(progress);

      // 위치 보간 (컨트롤 락 된 상태이므로 수동 세팅)
      const currentOffset = new THREE.Vector3().lerpVectors(offsetRear, offsetSide, eased);
      camera.position.copy(truckGroup.position).add(currentOffset);

      // 카메라는 차량 중앙 높이를 쳐다봄
      const targetLook = truckGroup.position.clone().add(new THREE.Vector3(0, 0.8, 1.5));
      camera.lookAt(targetLook);

      // 앞 범퍼(local Z=2.5)가 Z=0(브레이크 라인)을 돌파하는 순간 제동 페이즈 전환
      if (truckGroup.position.z + 2.5 >= BRAKE_Z) {
        truckGroup.position.z = BRAKE_Z - 2.5; // 앞 범퍼가 시작선에 정확히 정렬되도록 오차 제거
        simulationState = 'braking';
        setBrakeLightsActive(true);
        updateDashboard();
      }
    }
    else if (simulationState === 'braking') {
      // 제동 페이즈: F = m * a  => a = F_brake / m
      const deceleration = F_BRAKE / mass;
      brakingElapsedTime += dt;

      const t = brakingElapsedTime;
      const stopTime = initialSpeedMps / deceleration;

      if (t >= stopTime) {
        // 완전히 멈춤 (앞 범퍼 기준의 위치 산정)
        truckGroup.position.z = (Math.pow(initialSpeedMps, 2) / (2 * deceleration)) - 2.5;
        currentSpeedMps = 0;
        simulationState = 'stopped';
        showResults();
      } else {
        // 감속 주행 공식: x(t) = v0*t - 0.5*a*t^2 (앞 범퍼 Z=0 기점으로 셋팅)
        truckGroup.position.z = -2.5 + (initialSpeedMps * t) - (0.5 * deceleration * Math.pow(t, 2));
        currentSpeedMps = initialSpeedMps - (deceleration * t);

        // 남은 속도에 맞추어 바퀴 회전 속도도 서서히 정지
        const wheelRotSpeed = currentSpeedMps / WHEEL_RADIUS;
        wheels.forEach(w => w.rotation.x += wheelRotSpeed * dt);

        // 대시보드 실시간 거리 갱신 (제동 거리는 앞 범퍼 위치 Z + 2.5)
        safeSetText('actual-distance-value', (truckGroup.position.z + 2.5).toFixed(2));

        // 실시간 속도 기준으로 대시보드 에너지 갱신
        const currentKmh = currentSpeedMps * 3.6;
        updateDashboard(currentKmh);
      }

      // 제동 구간에서는 계속 고정된 카메라 Side view 유지하며 차량 추적
      camera.position.copy(truckGroup.position).add(offsetSide);

      // 타겟 지점도 부드럽게 추종
      const targetLook = truckGroup.position.clone().add(new THREE.Vector3(0, 0.8, 1.5));
      camera.lookAt(targetLook);

      // 제동등 포인트 조명 위치 갱신
      if (simulationState === 'braking') {
        brakePointLight.position.set(0, 0.8, truckGroup.position.z - 3.3);
      }
    }
    else {
      // IDLE 이나 STOPPED 상태에서는 사용자가 자유롭게 카메라 회전 및 탐색을 즐기게끔 controls만 주기적으로 업데이트
      controls.update();
    }
  }

  // 2. 최종 프레임 렌더링
  renderer.render(scene, camera);
}

// --- 창 크기 대응 반응형 뷰포트 처리 ---
function onWindowResize() {
  const container = document.getElementById('canvas-container');
  camera.aspect = container.clientWidth / container.clientHeight;
  camera.updateProjectionMatrix();
  renderer.setSize(container.clientWidth, container.clientHeight);
}

// --- 교사용 실시간 물리학 프레임 업데이트 ---
function updateTeacherPhysics(dt) {
  if (simulationState === 'idle') {
    controls.update();
    updateStudentNameTags();
    return;
  }

  let allStopped = true;

  activeStudents.forEach((student) => {
    const state = studentReadyStates[student.id] || { mass: 1500, speed: 50 };
    const initialSpeedMps = state.speed / 3.6;
    const deceleration = F_BRAKE / state.mass;

    let curState = studentCarStates[student.id] || 'idle';
    let curZ = studentCarPositions[student.id] !== undefined ? studentCarPositions[student.id] : START_Z;
    let curSpeed = studentCarSpeeds[student.id] !== undefined ? studentCarSpeeds[student.id] : initialSpeedMps;
    let curTime = studentCarTimes[student.id] || 0;

    if (curState === 'driving') {
      allStopped = false;
      curZ += initialSpeedMps * dt;

      if (curZ + 2.5 >= BRAKE_Z) {
        curZ = BRAKE_Z - 2.5;
        curState = 'braking';
        curTime = 0;
      }
    } else if (curState === 'braking') {
      allStopped = false;
      curTime += dt;
      const stopTime = initialSpeedMps / deceleration;

      if (curTime >= stopTime) {
        curZ = (Math.pow(initialSpeedMps, 2) / (2 * deceleration)) - 2.5;
        curSpeed = 0;
        curState = 'stopped';

        // 리더보드 결과 누적
        const finalDistance = curZ + 2.5;
        const offset = finalDistance - targetDistance;
        addStudentResult({
          studentId: student.id,
          nickname: student.nickname,
          mass: state.mass,
          speed: state.speed,
          actualDistance: finalDistance,
          offsetDistance: offset
        });
      } else {
        curZ = -2.5 + (initialSpeedMps * curTime) - (0.5 * deceleration * Math.pow(curTime, 2));
        curSpeed = initialSpeedMps - (deceleration * curTime);
      }
    }

    studentCarStates[student.id] = curState;
    studentCarPositions[student.id] = curZ;
    studentCarSpeeds[student.id] = curSpeed;
    studentCarTimes[student.id] = curTime;
  });

  // 3D 객체 인스턴스 갱신 및 2D 닉네임 꼬리표 갱신
  updateInstancedTrucks();
  updateStudentNameTags();

  // 모든 참여 차량의 평균 Z 위치 계산 (카메라 추적 타겟)
  let totalZ = 0;
  activeStudents.forEach(student => {
    totalZ += studentCarPositions[student.id] !== undefined ? studentCarPositions[student.id] : START_Z;
  });
  const avgZ = activeStudents.length > 0 ? (totalZ / activeStudents.length) : START_Z;

  // 카메라 트래킹 설정
  const roadWidth = activeStudents.length === 1 ? 10 : activeStudents.length * 3.5 + 4;
  const camX = - (roadWidth / 2 + 18);
  const camY = 12;

  if (simulationState === 'driving' || simulationState === 'braking') {
    camera.position.set(camX, camY, avgZ + 5);
    camera.lookAt(new THREE.Vector3(0, 1.5, avgZ));
    controls.target.set(0, 1.5, avgZ);
  } else {
    controls.update();
  }

  // 모든 차량이 멈췄을 때 최종 리더보드로 전환
  if (allStopped && activeStudents.length > 0 && (simulationState === 'driving' || simulationState === 'braking')) {
    simulationState = 'stopped';
    const activeEl = document.getElementById('teacher-active');
    if (activeEl) activeEl.style.display = 'none';
    const lbSection = document.getElementById('teacher-leaderboard-section');
    if (lbSection) lbSection.style.display = 'flex';

    controls.enabled = true;
    controls.target.set(0, 1.5, avgZ);
    controls.update();
  }
}

// --- 사용자 친화적 입력 동기화 헬퍼 함수 ---
function syncInputs(type, value) {
  let val = parseInt(value);
  if (isNaN(val)) return;

  const massSlider = document.getElementById('mass-slider');
  const speedSlider = document.getElementById('speed-slider');

  if (type === 'mass') {
    if (val >= 1000 && val <= 5000) {
      mass = val;
      if (massSlider) massSlider.value = val;
      updateCargoBoxes(mass);
    } else {
      // 타이핑 도중 즉시 잘려 나가는 불쾌감을 없애기 위해 값만 clamped 임시 적용
      mass = Math.max(1000, Math.min(5000, val));
      if (massSlider) massSlider.value = mass;
      updateCargoBoxes(mass);
    }
  } else if (type === 'speed') {
    if (val >= 10 && val <= 150) {
      initialSpeedKmh = val;
      if (speedSlider) speedSlider.value = val;
    } else {
      initialSpeedKmh = Math.max(10, Math.min(150, val));
      if (speedSlider) speedSlider.value = initialSpeedKmh;
    }
  }
  updateDashboard();
}
window.syncInputs = syncInputs;

