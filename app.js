// --- 교사용 세션 생성 및 초기화 ---
async function initTeacherSession() {
  const DEFAULT_PIN = "802935";
  sessionPin = DEFAULT_PIN;

  if (!supabaseClient) {
    alert("수파베이스가 연결되지 않았습니다. app.js에서 SUPABASE_URL 및 SUPABASE_ANON_KEY를 설정해 주세요.");
    return;
  }

  try {
    // 1. 기존 동일 고정 PIN의 세션 삭제 (중복 오류 예방 및 데이터 초기 청소)
    // 외래 키 ON DELETE CASCADE 설정으로 인해 results 테이블 데이터도 연쇄 삭제됩니다.
    await supabaseClient
      .from('sessions')
      .delete()
      .eq('pin_code', DEFAULT_PIN);

    // 2. 기본 고정 PIN으로 새 세션 등록 시도 (status: LOBBY)
    const { error } = await supabaseClient
      .from('sessions')
      .insert([{
        pin_code: sessionPin,
        target_distance: targetDistance,
        control_mode: 'BOTH',
        status: 'LOBBY'
      }]);

    if (error) {
      console.warn("기본 핀코드('802935') 등록 중복 또는 에러로 난수 핀코드로 우회합니다:", error);
      // Fallback: 6자리 난수 PIN 번호 생성
      sessionPin = Math.floor(100000 + Math.random() * 900000).toString();
      const fallbackRes = await supabaseClient
        .from('sessions')
        .insert([{
          pin_code: sessionPin,
          target_distance: targetDistance,
          control_mode: 'BOTH',
          status: 'LOBBY'
        }]);
      if (fallbackRes.error) throw fallbackRes.error;
    }

    document.getElementById('teacher-pin-value').innerText = sessionPin;

    // 3. 실시간 채널 구독 및 Presence 연결
    setupTeacherRealtime();

    // 초기 제어 모드 설정 및 슬라이더 가동 범위 초기 세팅
    setControlMode('BOTH');

  } catch (err) {
    console.error("수파베이스 세션 등록 실패:", err);
    alert("수파베이스 세션 등록에 실패했습니다. DB 테이블(sessions, results)이 생성되어 있는지 확인하세요.");
  }
}

// --- 교사용 실시간 소켓/Presence 바인딩 ---
function setupTeacherRealtime() {
  realtimeChannel = supabaseClient.channel(`room_${sessionPin}`, {
    config: { presence: { key: sessionPin } }
  });

  realtimeChannel
    .on('presence', { event: 'sync' }, () => {
      // 대기실 실시간 접속 학생 동기화
      const state = realtimeChannel.presenceState();
      students = [];
      Object.keys(state).forEach(key => {
        state[key].forEach(p => {
          if (p.role === 'student') {
            students.push({ id: p.id, nickname: p.nickname });
          }
        });
      });
      updateStudentLobbyUI();
    })
    .on('broadcast', { event: 'student-ready' }, ({ payload }) => {
      // 학생의 슬라이더 조작 준비 완료 상태 수신
      studentReadyStates[payload.studentId] = {
        mass: payload.mass,
        speed: payload.speed,
        isReady: payload.isReady
      };
      updateTeacherReadyStatus();
    })
    .on('broadcast', { event: 'student-result' }, ({ payload }) => {
      // 학생 개별 제동 완료 정보 수신 (실시간 리더보드 반영)
      addStudentResult(payload);
    })
    .subscribe(async (status) => {
      if (status === 'SUBSCRIBED') {
        // 교사로 접속 정보 트래킹
        await realtimeChannel.track({ role: 'teacher', id: 'teacher' });
      }
    });
}

